
public class Percentage
{
	public static void main(String[] args) 
	{
		float maths = 95;
		float science = 82;
		float physics = 73;
		float english = 94;
		float social_science = 65;

		float sum = maths + science + physics + english + social_science;
		System.out.println(sum);

		float total = 500;

		float per = (sum / total) * 100;

		System.out.println(per);





	}

}

